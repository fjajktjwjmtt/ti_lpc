// Get chip parameters by type
inline const ChipParams* getChipParams(ChipType type) {
    switch (type) {
        case CHIP_TMS5100: return &TMS5100_PARAMS;
        case CHIP_TMS5110: return &TMS5110_PARAMS;
        case CHIP_TMS5200: return &TMS5200_PARAMS;
        case CHIP_TMS5220: return &TMS5220_PARAMS;
        default: return nullptr;
    }
}

// Get chip type from name string
inline ChipType getChipTypeFromName(const char* name) {
    if (!name) return CHIP_UNKNOWN;
    
    // Check for various name formats
    if (strstr(name, "5100") != nullptr) return CHIP_TMS5100;
    if (strstr(name, "5110") != nullptr) return CHIP_TMS5110;
    if (strstr(name, "5200") != nullptr) return CHIP_TMS5200;
    if (strstr(name, "5220") != nullptr) return CHIP_TMS5220;
    
    return CHIP_UNKNOWN;
}

// Get chip parameters by name string
inline const ChipParams* getChipParamsByName(const char* name) {
    return getChipParams(getChipTypeFromName(name));
}

